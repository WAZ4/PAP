                        <p>
                            O segredo de se usar óleos essenciais com segurança é sempre observar as sugestões apropriadas de dosagem. Quer você esteja use o óleo essencial de modo aromático, tópico ou internamente, assegurar o uso da quantidade correta permitirá que você fique dentro dos limites do uso seguro. No uso tópico sempre existe o risco de irritação ou sensibilidade da pele, principalmente quando considerarmos que cada óleo essencial tem um perfil químico diferente e cada pessoa tem um nível diferente de sensibilidade e condições de pele e preferências diferentes. Lembre-se de que a dose de um óleo essencial sempre dependerá da idade, do tamanho, da condição de saúde e da sensibilidade pessoal da pele da pessoa. Se você tiver dúvidas específicas sobre sua pele, sempre é uma boa ideia consultar o médico antes de usar óleos essenciais de modo tópico. </p>
