                        <h3> DILUIÇÃO </h3>
                        <p>
                            Quando se trata do uso tópico seguro dos óleos essenciais, a diluição é um dos princípios mais importantes que é preciso entender. Algumas pessoas acham que diluir um óleo essencial antes de aplicá-lo vai diminuir seus benefícios; contudo, a diluição, na realidade, facilita a absorção pela pele, estendendo os benefícios por mais tempo. Ao diluir o óleo essencial com um óleo-base, você diminui a taxa de evaporação, melhora a absorção pela pele e protege a pele sensível ou delicada. Como você pode ver, a diluição não diminui a experiência com o óleo essencial, mas permite uma aplicação tópica mais eficaz. </p>
