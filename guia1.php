                            Os óleos essenciais são substâncias lipídicas solúveis, o que lhes permite penetrar na pele com facilidade. A aplicação tópica é um modo eficaz de se utilizar os óleos essenciais, porque assim que são absorvidos pela pele, permanecem na região aplicada, permitindo aos usuários alcançar benefícios em uma área localizada. </p>
