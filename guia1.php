                        <p>
                            Como os óleos essenciais são muito potentes, sempre é uma boa ideia usar algumas pequenas doses periodicamente ao longo do dia, em vez de uma grande dose única de uma só vez. No uso tópico, a dose pode ser repetida a cada quatro a seis horas, conforme necessário. </p>
